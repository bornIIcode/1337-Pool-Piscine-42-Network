char	*ft_strcapitalize(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		while ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= '0' && str[i] <= '9'))
		{
			i++;
			while (str[i] >= 'A' && str[i] <= 'Z')
			{
				str[i] += 32;
				i++;
			}
		}
		i++;
	}
	return (str);
}
